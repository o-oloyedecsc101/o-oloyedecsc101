fn main() {
   let num1 = 10;
   let num2 = 2;
   let mut results:i32;

   results = num1 +num2;
   println!("Sum: {}",results);

   results = num1 - num2;
   println!("Difference: {}",results );

   results = num1*num2;
   println!("Product : {}",results);

   results = num1/num2 ;
   println!("Quotient: {}",results);

   results = num1%num2;
   println!("Remainder:  {}",results);
}
